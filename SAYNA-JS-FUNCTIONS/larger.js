let larger = function(nombre1,nombre2){
    if (nombre1>nombre2){
        return nombre1;
    }
    else return nombre2;
 
}

console.log(larger(256, 400)) ; // 400
console.log(larger(31, 4)) ; // 31
console.log(larger(-6, 7)) ; // 7
console.log(larger(11.3, 11.2)) ; // 11.3
console.log(larger(-10, -3)) ; // -3
